# Updated for 2023. The year must be updated in the compute_tax_table method.
#
# To update, go to the 1040 HTML instructions and copy the tax table directly
# below. The commas and thousands separators are processed automatically.

module TaxTable
  def self.parse_table(table)
    res = []
    table.each_line do |line|
      line.chomp!
      next unless line =~ /\s/
      res.push(line.gsub(",", "").split(/\s+/).map(&:to_i))
    end
    return res
  end

  def compute_tax_table(income, status)

    if year != 2023
      raise 'Tax table must be updated'
    end

    TAX_TABLE.each do |row|
      if income >= row[0] && income < row[1]
        return row[status.tax_table_column]
      end
    end
    raise "Income #{income} not found in tax table"
  end

